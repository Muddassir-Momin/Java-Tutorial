//Multiplication of Two Number    Self Define Program

public class MultiplicationSelfDefine             //class (File name)
{
    public static void main(String[] args)          //Main Function
    {                                              //Starting of main Function
       int a=10,b=20;                                // int is a Datatype Which is use for Storing the Integer Values
       int c=a*b;                                 // This is Expression   where We are multiplication  a and b and Stores in C
       System.out.println(c);                    // System.out.print is used for printing the Value After Multiplication Which Stored in C
       System.out.println("Multiplication="+c);
       System.out.println(a+"*"+b+"="+c);            // here we printed a,b value and Result a*b=c 
    }                                       // End of Main Function                          end Compilation
}                                                // End of Class

