//Self Define Division Program

public class DivSelfDefine {
    public static void main(String[] args) {
        int a = 20, b = 20; // int is a Datatype Which is use for Storing the Integer Values a,b is Memory address
        int c = a / b; // This is Expression where We are division a and b and Stores in C
        System.out.println(c); // System.out.print is used for printing the Value After Division Which Stored in C
        System.out.println("Division =" + c);
        System.out.println(a + "/" + b + "=" + c); // here we printed a,b value and Result a*b=c
    }
}

